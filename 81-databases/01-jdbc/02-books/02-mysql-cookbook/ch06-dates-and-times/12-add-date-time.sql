SELECT t1, SEC_TO_TIME(TIME_TO_SEC(t1) + 7200) AS 't1 plus 2 hours' FROM time_val;
/*
two hours is 7,200 seconds (2 × 60 × 60)
+----------+-----------------+
| t1       | t1 plus 2 hours |
+----------+-----------------+
| 15:00:00 | 17:00:00        |
| 05:01:30 | 07:01:30        |
| 12:30:20 | 14:30:20        |
+----------+-----------------+
3 rows in set (0.00 sec)
*/

